package com.db1.db1start;

public class NumeroInteiro {
	
	public static int operacaoSoma(Integer numero1, Integer numero2) {
		Integer soma = numero1 + numero2;
		return soma;
	}
	
	public static int operacaoSubtracao(Integer numero1, Integer numero2) {
		Integer subtracao = numero1 - numero2;
		return subtracao;
	}
}
